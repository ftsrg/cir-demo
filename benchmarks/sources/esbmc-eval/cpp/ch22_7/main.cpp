// Fig. 22.2: fig22_02.cpp
// Demonstrating operator reinterpret_cast.
#include <iostream>

using std::cout;
using std::endl;

int main()
{
   int x = 120;
   int *ptr = &x;

   // use reinterpret_cast to cast from int * to char *
   cout << *reinterpret_cast< char * >( ptr ) << endl;

   return 0;

}  // end main
 
