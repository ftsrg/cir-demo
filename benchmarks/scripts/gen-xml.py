"""
Generate one BenchExec benchmark definition (xml/<id>.xml) per SV-COMP
C.ReachSafety verifier. The tool set comes from category-structure.yml and the
per-tool module/options/version from fm-tools (see _fmtools.py).

Each tool gets the BenchExec tool-info module and the competition-faithful
command-line options recorded in fm-tools, so adding/updating a tool is a matter
of fm-tools data, not hand-edited XML.

Rundefinitions per tool:
  * esbmc-kind, cbmc, divine -- these verify the original C++ directly, so they
    run all four configs: the original suite (cpp-baseline) plus all three
    cir2c-mapped C variants (c-havoc_std, c-exact_std, c-nohavoc_std).
  * every other tool is C-only, so it runs just the three mapped variants and is
    compared against the ESBMC baseline at table-generation time.

This three-tools-get-the-original-suite split is the ONE hardcoded fact in the
whole benchmark setup; the Makefile re-derives it from the generated XML (which
configs each file declares), so it stays the single source of truth.

Usage:
    python3 gen-xml.py [--clean] [<tool-id> ...]
        no ids   -> generate for all C.ReachSafety verifiers
        --clean  -> remove existing xml/*.xml first (drops tools no longer listed)
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path
from xml.sax.saxutils import quoteattr

import _fmtools

XML_DIR = _fmtools.BENCHMARKS_DIR / "xml"

# Tools able to verify the original C++ suite, hence the full four-config run.
CPP_CAPABLE_IDS = {"esbmc-kind", "cbmc", "divine"}

CONFIGS_WITH_CPP = ["cpp-baseline", "c-havoc_std", "c-exact_std", "c-nohavoc_std"]
CONFIGS_C_ONLY = ["c-havoc_std", "c-exact_std", "c-nohavoc_std"]

# Per-run resource budget: the standard SV-COMP limits (15 GB RAM, 8 processing
# units; the 900 s CPU-time limit is applied by the Makefile via --timelimit).
# These match the competition options fm-tools records (e.g. CPAchecker's
# `--heap 10000M`), so tools run as they do in SV-COMP.
MEMLIMIT = "15 GB"
CPU_CORES = "8"

DOCTYPE = (
    '<!DOCTYPE benchmark PUBLIC "+//IDN sosy-lab.org//DTD BenchExec benchmark 3.30//EN"\n'
    '          "https://www.sosy-lab.org/benchexec/benchmark-3.30.dtd">'
)


def rundefinition(name: str, options: list[str]) -> str:
    opts = "".join(f'    <option name={quoteattr(o)}/>\n' for o in options)
    return (
        f'  <rundefinition name="{name}">\n'
        f'{opts}'
        f'    <tasks name="{name}">\n'
        f'      <includesfile>../sets/{name}.set</includesfile>\n'
        f'      <propertyfile>../sources/{name}/unreach-call.prp</propertyfile>\n'
        f'    </tasks>\n'
        f'  </rundefinition>\n'
    )


def build_xml(info: dict) -> str:
    configs = CONFIGS_WITH_CPP if info["id"] in CPP_CAPABLE_IDS else CONFIGS_C_ONLY
    body = "\n".join(rundefinition(c, info["options"]) for c in configs)
    return (
        '<?xml version="1.0"?>\n'
        f'{DOCTYPE}\n'
        f'<!-- Generated by gen-xml.py from fm-tools ({info["id"]} {info["version"]}).\n'
        f'     Edit fm-tools data or gen-xml.py and regenerate; do not edit by hand. -->\n'
        f'<benchmark tool={quoteattr(info["module"])}\n'
        f'           memlimit="{MEMLIMIT}"\n'
        f'           cpuCores="{CPU_CORES}">\n\n'
        f'{body}\n'
        '</benchmark>\n'
    )


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("ids", nargs="*", help="tool ids (default: all C.ReachSafety verifiers)")
    ap.add_argument("--clean", action="store_true",
                    help="remove existing xml/*.xml before generating")
    args = ap.parse_args()

    XML_DIR.mkdir(exist_ok=True)
    if args.clean:
        for f in XML_DIR.glob("*.xml"):
            f.unlink()

    ids = args.ids or _fmtools.reachsafety_ids()
    written = 0
    for tid in ids:
        info = _fmtools.tool_info(tid)
        if not info:
            print(f"  [skip] {tid}: not a {_fmtools.TARGET_COMPETITION} "
                  f"Verification-track C verifier", file=sys.stderr)
            continue
        if not info["module"]:
            print(f"  [skip] {tid}: no BenchExec tool-info module", file=sys.stderr)
            continue
        (XML_DIR / f"{tid}.xml").write_text(build_xml(info), encoding="utf-8")
        written += 1
    print(f"Wrote {written} benchmark definition(s) to {XML_DIR}/")
    return 0


if __name__ == "__main__":
    sys.exit(main())
